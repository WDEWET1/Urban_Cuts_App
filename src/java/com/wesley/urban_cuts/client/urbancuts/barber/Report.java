    private void populate_combo_boxes() {
        
        // populate barber combo box
        List<Payment> allPayments = paymentCrudService.findAll();
        ArrayList<String> barbers = new ArrayList<>();
        
        for ( int i = 0; i < paymentCrudService.count(); i++ ){  
            barbers.add(allPayments.get(i).getBarber_name());
        }
        barbers.toArray();
        Set<String> barb = (Set<String>) new LinkedHashSet<>(barbers);
        
        jComboBox1.setModel(new DefaultComboBoxModel(barb.toArray()));

        // populate month combo box
        months = new ArrayList<>();
        months.add("Jan");
        months.add("Feb");
        months.add("Mar");
        months.add("Apr");
        months.add("May");
        months.add("Jun");
        months.add("Jul");
        months.add("Aug");
        months.add("Sept");
        months.add("Oct");
        months.add("Nov");
        months.add("Dec");
        
        jComboBox2.setModel(new DefaultComboBoxModel(months.toArray()));
    }
