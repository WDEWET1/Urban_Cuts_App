    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        List<Payment> allPayments = paymentCrudService.findAll();
        int total_cuts = 0;
        int total_all = 0;
        double total_value = 0.00;
        double total_value_all = 0.00;
        int user_month_temp = jComboBox2.getSelectedIndex();
        user_month_temp++;
        String user_month = "0"+user_month_temp;
        
        DateFormat dateFormat = new SimpleDateFormat("MM");
        String db_month="";

        for ( int i = 0; i < paymentCrudService.count(); i++ ){  
            //get total per barber, per month
            db_month = dateFormat.format(allPayments.get(i).getDate());
            
            if(user_month.equals(db_month)){
                if(allPayments.get(i).getBarber_name().equals(jComboBox1.getSelectedItem().toString())){
                    total_cuts++;
                    total_value += allPayments.get(i).getPrice();
                }
                //get total values for all barbers for a given month
                total_all++;
                total_value_all += allPayments.get(i).getPrice();
            }
        }
        jTextField1.setText(""+total_cuts);
        jTextField2.setText("R"+total_value);
        jTextField3.setText(""+total_all);
        jTextField4.setText("R"+total_value_all);
        jTextField5.setText(""+db_month);
    }//GEN-LAST:event_jButton2ActionPerformed

